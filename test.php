<p> Linear algebra is everywhere in the application of mathematics.  Huge linear systems are solved all the time on computers, and the science and engineering world depends on it.  This course will describe how matrices and vectors can be handled in a stable, fast, and accurate manner.  We will place these topics in their correct mathematical context, namely finite-dimensional vector spaces.  However, the emphasis will be on geometric, algorithmic, and practical understanding.</p>

<p><img src="eleventwo.png" style="float:right" height="140" alt="plot showing least-squares polynomial fit">We will cover these tools and topics:<br></p>

<ul>
  <li>singular value decomposition (SVD), a.k.a. principal component analysis</li>
  <li>conditioning of problems and stability of algorithms</li>
  <li>Gauss elimination and its variants for structured matrices</li>
  <li>over- and under-determined systems</li>
  <li>least squares methods</li>
  <li>power and QR methods for eigenvalues</li>
  <li>Krylov subspace methods, iterative linear algebra</li>
</ul>

<img src="wilkinson.svg" style="float:left" height="160" alt="plot showing zeros of perturbed polynomials"> <p>We will use Matlab (or free tools Octave or Python). Most lectures and homework assignments will have nontrivial computational examples. Introductory Matlab examples at the beginning of the course will bring students up to speed with that tool. And there will be some proofs; they will make up about 30% of the homework.</p>

<p>The course is for graduate students and advanced undergraduates in all fields with a need for computation: statistics, computer science, geophysics, engineering, biology. Students from mathematics will see what they are missing in undergraduate linear algebra and graduate algebra.</p>

<p><b>Prerequisites:&nbsp;</b> Officially,<br>
&nbsp;&nbsp; &nbsp;&nbsp; <span style="font-style: italic;">MATH F314 or equivalent or permission of the instructor. Recommended: MATH F421 or MATH F401.</span><br>
Because you might be a graduate student with a background from another University, I translate this as: A course in linear algebra plus some exposure to calculus and/or analysis beyond the standard freshman/sophomore sequence in calculus.</p>

